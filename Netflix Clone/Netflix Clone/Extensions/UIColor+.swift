import Foundation
import SwiftUI

extension UIColor {
    static var dark: UIColor {
        return UIColor(red: 18/255, green: 18/255, blue: 18/255, alpha: 1)
    }
    static var unselectedGrey: UIColor {
        return UIColor(red: 140/255, green: 135/255, blue: 135/255, alpha: 1)
    }
}
